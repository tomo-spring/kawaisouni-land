Game.drawFlowerBearScreen = function(x, y, time) {
  var ctx = Game.ctx;
  var rect = Game.rect;
  var sw = 132;
  var sh = 104;
  rect(x, y, sw, sh, "#ffffff");
  Game.strokeRect(x, y, sw, sh, "#aaaaaa", 2);

  ctx.save();
  ctx.beginPath();
  ctx.rect(x, y, sw, sh);
  ctx.clip();

  var s = 1.5;
  var walkX = Math.sin(time * 0.002) * 8;
  var bobY = Math.abs(Math.sin(time * 0.006)) * 2;
  var ox = x + 28 + walkX;
  var oy = y + 2 - bobY;

  var outline = Game._flowerBearOutline;
  for (var i = 0; i < outline.length; i++) {
    rect(ox + outline[i][1] * s, oy + outline[i][0] * s, s, s, "#2a2a2a");
  }

  var colors = Game._flowerBearColors;
  for (var color in colors) {
    var pixels = colors[color];
    for (var j = 0; j < pixels.length; j++) {
      rect(ox + pixels[j][1] * s, oy + pixels[j][0] * s, s, s, color);
    }
  }

  ctx.restore();
};
